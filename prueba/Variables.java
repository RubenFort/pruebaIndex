
public class Variables {

	public static void main(String[] args) {
		
		//Declaro variables
		int entero =  10;
		Double doble = 22.5;
		char letra = 'd';
		String frase = "Hola mundo";
		
		//Imprimo variables por pantalla
		System.out.println("Valor del int: " + entero);
		System.out.println("Valor del Double: " + doble);
		System.out.println("Valor del char: " + letra);
		System.out.println("Valor del String: " + frase);
		
	}
}